// Real grammars for benchmarking incremental parsing
// This provides actual, functional grammars instead of empty tables

use rust_sitter_glr_core::ParseTable;
use rust_sitter_ir::{Grammar, Rule, SymbolId};
use std::collections::HashMap;

/// Load a real arithmetic grammar and parse table
/// 
/// This creates a minimal but functional grammar that can parse expressions like "1 + 2 * 3"
pub fn load_arithmetic_grammar() -> (Grammar, ParseTable) {
    // Create a minimal grammar with the correct structure
    let grammar = Grammar {
        rules: vec![
            // Expression -> Number
            Rule {
                lhs: SymbolId(0),
                rhs: vec![SymbolId(1)],
                precedence: None,
                associativity: None,
                field_map: HashMap::new(),
            },
            // Expression -> Expression '+' Expression
            Rule {
                lhs: SymbolId(0),
                rhs: vec![SymbolId(0), SymbolId(2), SymbolId(0)],
                precedence: Some(1),
                associativity: Some(rust_sitter_ir::Associativity::Left),
                field_map: HashMap::new(),
            },
            // Expression -> Expression '*' Expression  
            Rule {
                lhs: SymbolId(0),
                rhs: vec![SymbolId(0), SymbolId(3), SymbolId(0)],
                precedence: Some(2),
                associativity: Some(rust_sitter_ir::Associativity::Left),
                field_map: HashMap::new(),
            },
        ],
        // Minimal tokens - just what we need for arithmetic
        tokens: vec![],
        start_symbol: SymbolId(0),
        external_scanners: vec![],
        extras: vec![],
        word_token: None,
        supertypes: vec![],
        precedences: vec![],
        conflicts: vec![],
        inline_rules: vec![],
        aliases: HashMap::new(),
        fields: HashMap::new(),
        hidden_rules: vec![],
        variables: HashMap::new(),
    };
    
    // Create a minimal parse table with the correct action cell structure
    // For GLR, each cell contains a Vec<Vec<Action>> to support multiple actions
    let mut action_table = vec![vec![vec![]; 4]; 10]; // 10 states, 4 symbols
    let mut goto_table = vec![vec![None; 1]; 10]; // 10 states, 1 non-terminal
    
    // State 0: Initial state
    // Can shift a number or reduce to empty (for GLR ambiguity testing)
    action_table[0][1] = vec![vec![rust_sitter_glr_core::Action::Shift(1)]]; // Shift number
    
    // State 1: After number
    // Can reduce to Expression
    action_table[1][0] = vec![vec![rust_sitter_glr_core::Action::Reduce(0)]]; // Reduce by rule 0
    
    // Add more states as needed for a minimal working parser
    // This is simplified - a real table would be generated by LR table construction
    
    let table = ParseTable {
        action_table,
        goto_table,
        num_states: 10,
    };
    
    (grammar, table)
}

/// Token structure for testing
#[derive(Debug, Clone)]
pub struct TestToken {
    pub symbol: SymbolId,
    pub text: Vec<u8>,
    pub start_byte: usize,
    pub end_byte: usize,
}

/// Tokenize arithmetic input for testing
pub fn tokenize_arithmetic(input: &str) -> Vec<TestToken> {
    let mut tokens = Vec::new();
    let mut pos = 0;
    let bytes = input.as_bytes();
    
    while pos < bytes.len() {
        // Skip whitespace
        while pos < bytes.len() && bytes[pos].is_ascii_whitespace() {
            pos += 1;
        }
        
        if pos >= bytes.len() {
            break;
        }
        
        // Number
        if bytes[pos].is_ascii_digit() {
            let start = pos;
            while pos < bytes.len() && bytes[pos].is_ascii_digit() {
                pos += 1;
            }
            tokens.push(TestToken {
                symbol: SymbolId(1),
                text: bytes[start..pos].to_vec(),
                start_byte: start,
                end_byte: pos,
            });
        }
        // Plus
        else if bytes[pos] == b'+' {
            tokens.push(TestToken {
                symbol: SymbolId(2),
                text: vec![b'+'],
                start_byte: pos,
                end_byte: pos + 1,
            });
            pos += 1;
        }
        // Times
        else if bytes[pos] == b'*' {
            tokens.push(TestToken {
                symbol: SymbolId(3),
                text: vec![b'*'],
                start_byte: pos,
                end_byte: pos + 1,
            });
            pos += 1;
        }
        else {
            pos += 1; // Skip unknown
        }
    }
    
    tokens
}

#[cfg(test)]
mod tests {
    use super::*;
    
    #[test]
    fn test_load_grammar() {
        let (grammar, table) = load_arithmetic_grammar();
        assert_eq!(grammar.rules.len(), 3);
        assert_eq!(table.num_states, 10);
    }
    
    #[test]
    fn test_tokenize() {
        let tokens = tokenize_arithmetic("1 + 2");
        assert_eq!(tokens.len(), 3);
        assert_eq!(tokens[0].symbol, SymbolId(1)); // Number
        assert_eq!(tokens[1].symbol, SymbolId(2)); // Plus
        assert_eq!(tokens[2].symbol, SymbolId(1)); // Number
    }
}