/// Create the classic ambiguous expression grammar
/// E -> E + E | E * E | num
/// This grammar is ambiguous for inputs like "1 + 2 * 3"
fn create_ambiguous_grammar() -> Grammar {
    let mut grammar = Grammar::new("ambiguous_expr".to_string());

    // Tokens
    let num = SymbolId(1);
    let plus = SymbolId(2);
    let mult = SymbolId(3);
    let eof = SymbolId(0);

    grammar.tokens.insert(
        num,
        Token {
            name: "number".to_string(),
            pattern: TokenPattern::Regex(r"\d+".to_string()),
            fragile: false,
        },
    );

    grammar.tokens.insert(
        plus,
        Token {
            name: "plus".to_string(),
            pattern: TokenPattern::String("+".to_string()),
            fragile: false,
        },
    );

    grammar.tokens.insert(
        mult,
        Token {
            name: "mult".to_string(),
            pattern: TokenPattern::String("*".to_string()),
            fragile: false,
        },
    );

    // Non-terminal
    let expr = SymbolId(10);

    // Rules
    // E -> num
    let rule1 = Rule {
        lhs: expr,
        rhs: vec![Symbol::Terminal(num)],
        production_id: ProductionId(0),
        precedence: None,
        associativity: None,
        fields: vec![],
    };

    // E -> E + E
    let rule2 = Rule {
        lhs: expr,
        rhs: vec![
            Symbol::NonTerminal(expr),
            Symbol::Terminal(plus),
            Symbol::NonTerminal(expr),
        ],
        production_id: ProductionId(1),
        precedence: None,
        associativity: None,
        fields: vec![],
    };

    // E -> E * E
    let rule3 = Rule {
        lhs: expr,
        rhs: vec![
            Symbol::NonTerminal(expr),
            Symbol::Terminal(mult),
            Symbol::NonTerminal(expr),
        ],
        production_id: ProductionId(2),
        precedence: None,
        associativity: None,
        fields: vec![],
    };

    grammar.rules.insert(RuleId(0), rule1);
    grammar.rules.insert(RuleId(1), rule2);
    grammar.rules.insert(RuleId(2), rule3);

    // Add rule names
    grammar.rule_names.insert(expr, "expression".to_string());

    grammar
}

/// Create a parse table with conflicts for testing GLR
fn create_conflicting_parse_table() -> ParseTable {
    // This would normally be generated by the table generator
    // For testing, we create a simple table with shift/reduce conflicts

    let mut action_table = vec![vec![Action::Error; 4]; 10];
    let mut goto_table = vec![vec![StateId(0); 1]; 10];

    // State 0: Initial state
    // num -> shift to state 1
    action_table[0][1] = Action::Shift(StateId(1));

    // State 1: After seeing a number
    // + -> shift to state 2
    // * -> shift to state 3
    // EOF -> reduce by rule 0 (E -> num)
    action_table[1][2] = Action::Shift(StateId(2));
    action_table[1][3] = Action::Shift(StateId(3));
    action_table[1][0] = Action::Reduce(RuleId(0));

    // State 2: After E +
    // num -> shift to state 4
    action_table[2][1] = Action::Shift(StateId(4));

    // State 3: After E *
    // num -> shift to state 5
    action_table[3][1] = Action::Shift(StateId(5));

    // State 4: After E + num
    // This is where conflicts occur
    // + -> reduce by rule 1 (E -> E + E)
    // * -> shift to state 6 OR reduce by rule 1 (shift/reduce conflict!)
    // EOF -> reduce by rule 1
    action_table[4][2] = Action::Reduce(RuleId(1));
    action_table[4][3] = Action::Fork(vec![Action::Shift(StateId(6)), Action::Reduce(RuleId(1))]);
    action_table[4][0] = Action::Reduce(RuleId(1));

    // State 5: After E * num
    // + -> reduce by rule 2 (E -> E * E)
    // * -> reduce by rule 2
    // EOF -> reduce by rule 2
    action_table[5][2] = Action::Reduce(RuleId(2));
    action_table[5][3] = Action::Reduce(RuleId(2));
    action_table[5][0] = Action::Reduce(RuleId(2));

    // State 6: After E + E *
    // num -> shift to state 7
    action_table[6][1] = Action::Shift(StateId(7));

    // State 7: After E + E * num
    // Everything reduces by rule 2
    action_table[7][2] = Action::Reduce(RuleId(2));
    action_table[7][3] = Action::Reduce(RuleId(2));
    action_table[7][0] = Action::Reduce(RuleId(2));

    // Goto table for expr (symbol 10 -> index 0)
    goto_table[0][0] = StateId(8); // From state 0, expr goes to state 8
    goto_table[2][0] = StateId(4); // From state 2 (after +), expr goes to state 4
    goto_table[3][0] = StateId(5); // From state 3 (after *), expr goes to state 5

    // Accept state
    action_table[8][0] = Action::Accept;

    ParseTable {
        action_table,
        goto_table,
        symbol_metadata: vec![],
        state_count: 10,
        symbol_count: 11,
        symbol_to_index: IndexMap::new(),
    }
}

#[test]
fn test_glr_fork_creation() {
    let mut glr = GLRStack::new(StateId(0));

    // Test creating a fork with multiple actions
    let actions = vec![
        Action::Shift(StateId(1)),
        Action::Reduce(RuleId(0)),
        Action::Shift(StateId(2)),
    ];

    let fork_result = glr.fork(0, &actions).unwrap();

    // Should create 3 new stacks
    assert_eq!(fork_result.new_stacks.len(), 3);

    // Original stack should be marked for deactivation
    assert_eq!(fork_result.deactivate, vec![0]);

    // Add new stacks to GLR
    for stack in fork_result.new_stacks {
        glr.stacks.push(stack);
    }

    // Deactivate original
    glr.stacks[0].active = false;

    // Should have 3 active stacks
    let active = glr.active_stacks();
    assert_eq!(active.len(), 3);
}

#[test]
fn test_glr_merge() {
    let mut glr = GLRStack::new(StateId(0));

    // Create two stacks at the same state
    let stack1 = glr.stacks[0].clone();
    let mut stack2 = stack1.clone();
    stack2.id = 1;
    glr.stacks.push(stack2);

    // Check for merge opportunities
    let merge_groups = glr.check_merge(StateId(0), 0);
    assert_eq!(merge_groups.len(), 1);
    assert_eq!(merge_groups[0].len(), 2);

    // Perform merge
    let merged_idx = glr.merge(&merge_groups[0]).unwrap();
    assert_eq!(merged_idx, 0);

    // Second stack should be inactive
    assert!(!glr.stacks[1].active);
    assert_eq!(glr.active_stacks().len(), 1);
}

#[test]
fn test_ambiguous_expression_parsing() {
    let grammar = create_ambiguous_grammar();
    let parse_table = create_conflicting_parse_table();

    let mut parser = Parser::new(grammar, parse_table);

    // Parse "1 + 2 * 3"
    // This should create a fork at the shift/reduce conflict
    let input = "1 + 2 * 3";

    match parser.parse(input) {
        Ok(tree) => {
            println!("Parsed ambiguous expression: {:?}", tree);

            // Check if we got an ambiguity node
            if tree.field_name == Some("ambiguous".to_string()) {
                assert_eq!(tree.symbol, SymbolId(0xFFFF));
                assert!(tree.children.len() > 1);
                println!("Found {} parse interpretations", tree.children.len());
            }
        }
        Err(e) => {
            // This is expected with our simplified parse table
            println!("Parse error (expected): {:?}", e);
        }
    }
}

#[test]
fn test_glr_parser_coordinator() {
    let mut glr_parser = GLRParser::new(StateId(0));

    // Process a token that causes a fork
    let token_symbol = SymbolId(3); // mult token

    // Mock action getter that returns a fork
    let get_action = |state: StateId, symbol: SymbolId| -> Action {
        if state == StateId(4) && symbol == SymbolId(3) {
            Action::Fork(vec![Action::Shift(StateId(6)), Action::Reduce(RuleId(1))])
        } else {
            Action::Error
        }
    };

    // Simulate being in state 4
    glr_parser.stack.stacks[0]
        .state_stack
        .push(rust_sitter::glr::ParserState {
            state: StateId(4),
            symbol: None,
            position: 5,
        });

    // Process the token
    glr_parser.process_token(token_symbol, get_action).unwrap();

    // Should have created new stacks
    assert!(glr_parser.stack.stacks.len() > 1);
}

#[test]
fn test_dangling_else_grammar() {
    // Another classic ambiguous grammar: if-then-else
    let mut grammar = Grammar::new("if_then_else".to_string());

    let if_tok = SymbolId(1);
    let then_tok = SymbolId(2);
    let else_tok = SymbolId(3);
    let expr_tok = SymbolId(4);
    let stmt = SymbolId(10);

    // stmt -> if expr then stmt
    // stmt -> if expr then stmt else stmt
    // stmt -> expr

    let rule1 = Rule {
        lhs: stmt,
        rhs: vec![
            Symbol::Terminal(if_tok),
            Symbol::Terminal(expr_tok),
            Symbol::Terminal(then_tok),
            Symbol::NonTerminal(stmt),
        ],
        production_id: ProductionId(0),
        precedence: None,
        associativity: None,
        fields: vec![],
    };

    let rule2 = Rule {
        lhs: stmt,
        rhs: vec![
            Symbol::Terminal(if_tok),
            Symbol::Terminal(expr_tok),
            Symbol::Terminal(then_tok),
            Symbol::NonTerminal(stmt),
            Symbol::Terminal(else_tok),
            Symbol::NonTerminal(stmt),
        ],
        production_id: ProductionId(1),
        precedence: Some(Precedence::Named("else".to_string())),
        associativity: None,
        fields: vec![],
    };

    grammar.rules.insert(RuleId(0), rule1);
    grammar.rules.insert(RuleId(1), rule2);

    // The grammar is ambiguous for:
    // "if expr then if expr then stmt else stmt"
    // Could be:
    // 1. if expr then (if expr then stmt else stmt)
    // 2. if expr then (if expr then stmt) else stmt
}

#[test]
fn test_precedence_resolution() {
    // Test that precedence annotations can resolve ambiguity
    let mut grammar = create_ambiguous_grammar();

    // Add precedence to resolve ambiguity
    // mult has higher precedence than plus
    if let Some(plus_rule) = grammar.rules.get_mut(&RuleId(1)) {
        plus_rule.precedence = Some(Precedence::Level(1));
        plus_rule.associativity = Some(Associativity::Left);
    }

    if let Some(mult_rule) = grammar.rules.get_mut(&RuleId(2)) {
        mult_rule.precedence = Some(Precedence::Level(2));
        mult_rule.associativity = Some(Associativity::Left);
    }

    // With precedence, "1 + 2 * 3" should parse as "1 + (2 * 3)"
    // Not as "(1 + 2) * 3"
}
